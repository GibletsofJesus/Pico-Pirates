function	drawupdatewater()
	for i=1,#wpts do
		local vel = .975+(wpts[i]-prevwpts[i])*1.125
		prevwpts[i] = wpts[i]
		wpts[i]+=vel

		local diff = -0.095 * (
			pt(i+1) + pt(i-1)
			+pt(i+2) + pt(i-2)
			+pt(i+3) + pt(i-3)
			+pt(i+4) + pt(i-4)	) *
			(-8*wpts[i])

		wpts[i] -= diff*0.004

		wpts[i]=mid(wpts[i],0,128)

		line(i-1,127,i-1,wpts[i]+97,1)

		if vel>1.25 or vel<-1.25 then
			pset(i-1,wpts[i]+97,7)
		end
	end
end

